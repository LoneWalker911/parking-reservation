public class Duration {
    private int dur_id;
    private String dur_name;
    private String dur_type;
    private String dur_des;
    private String dur_time;

    /**
     * @return the dur_id
     */
    public int getDur_id() {
        return dur_id;
    }

    /**
     * @param dur_id the dur_id to set
     */
    public void setDur_id(int dur_id) {
        this.dur_id = dur_id;
    }

    /**
     * @return the dur_name
     */
    public String getDur_name() {
        return dur_name;
    }

    /**
     * @param dur_name the dur_name to set
     */
    public void setDur_name(String dur_name) {
        this.dur_name = dur_name;
    }

    /**
     * @return the dur_type
     */
    public String getDur_type() {
        return dur_type;
    }

    /**
     * @param dur_type the dur_type to set
     */
    public void setDur_type(String dur_type) {
        this.dur_type = dur_type;
    }

    /**
     * @return the dur_des
     */
    public String getDur_des() {
        return dur_des;
    }

    /**
     * @param dur_des the dur_des to set
     */
    public void setDur_des(String dur_des) {
        this.dur_des = dur_des;
    }

    /**
     * @return the dur_time
     */
    public String getDur_time() {
        return dur_time;
    }

    /**
     * @param dur_time the dur_time to set
     */
    public void setDur_time(String dur_time) {
        this.dur_time = dur_time;
    }
}
